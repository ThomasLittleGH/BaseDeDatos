    // metodo que permite buscar el usuario y devolver si fue encontrado
    boolean buscarDatos(int buscar){
        boolean encontrado = false;
        // SE pone en un string el comando SQL
        String sql = "SELECT * FROM user WHERE id='"+buscar+"'";
        try{
            // Se definen instrucciones para ejecutar las sentencias SQL
            Statement st = conn.createStatement(); //obj para realiza la conexion
            ResultSet rs = st.executeQuery(sql); // se ejecuta el query del sql
            while (rs.next()){ // navega por todos los registros
                if (Integer.parseInt(txtId.getText()) == rs.getInt(1)) { // el get getString captura el valor del campo 1 de la tabla
                    if(txtPwd.getText().equals(rs.getString(2))){ // el comando compara las claves
                        encontrado = true;
                    }
                }
            }
        } catch (Exception e){
            System.out.println(e.getMessage());
        }
        
        return encontrado;
    }
    
